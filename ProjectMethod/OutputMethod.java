
public class OutputMethod {

	// return data type String
	public static String a() {
		// ...
		return "a";
	}
	
	public static int one() {
		return 1;
	}
	
	// void = 리턴 값이 없다 
	public static void main(String[] args) {
		
		System.out.println(a()); // String "a"
		System.out.println(one()); // int 1

	}

}
